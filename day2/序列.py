#序列切片:序列名[起始下标，结束下标（不包含这个下标），步长]
#对list进行切片
my_list=[0,1,2,3,4,5,6]
result1=my_list[1:4]
print(f"结果为：{result1}")
#对tuple进行切片
my_tuple=(0,1,2,3,4,5,6)
result2=my_tuple[1:4]
print(f"结果为：{result2}")
#对字符串进行切片
my_str="0123456"
result3=my_str[::2]
print(f"结果为：{result3}")
result4=my_str[::-1]
print(f"结果为：{result4}")
#倒序输出
str="轻舟已过万重山，我是黑马"
result5=str[::-1][5:10]
print(result5)